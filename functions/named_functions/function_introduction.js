/*
 * Functions are reusable pieces of code that perform a specific task or calculate a value.
 * Think of functions as a machine that takes some input, does some operations on it,
 * and then produces an output.
 * */

function greet() {
	console.log("Hello, Jessica!");
}

/* functions require a function name
 * We have declared a function called greet.
 * nside that function, we have a console.log that logs 
 * the message Hello, Jessica!
 *  If we tried to run this code, 
 *  we would not see the message appear in the console.
 *  This is because we need to call the function
 * */

/* A function call, or invocation, is when we actually use or execute the function.
 * To call a function, you will need to reference the function name followed by
 * a set of parentheses:
 * */

greet();
