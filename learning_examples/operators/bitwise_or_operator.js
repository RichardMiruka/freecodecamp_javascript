/* The bitwise OR (|) operator returns a 1 in each bit position
 * for which the corresponding bits of either or both operands are 1.
 */

let a = 5; // Binary: 101
let b = 3; // Binary: 011
console.log(a | b); // Output: 7 (Binary: 111)

// the result is 7 (111 in binary) because at least one of the bits is 1 in each position.
