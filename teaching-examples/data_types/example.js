// JavaScript has two main categories of data types:
//  primitive and non-primitive.
// Primitive data types are the basic building blocks of JavaScript.
// Non-primitive data types are objects that contain multiple values.

// Primitive Data Types Examples:
// string - "Hello World"
// number - 42
// boolean - true or false
// null - represents no value
// undefined - a variable that has been declared but not assigned a value
// symbol - a unique and immutable data type

// Non-Primitive Data Types Example:
// object - { name: "Alice", age: 30 }
// array - [1, 2, 3, 4, 5]
// function - function add(a, b) { return a + b; }